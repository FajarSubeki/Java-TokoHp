    private void tcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyPressed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NO");
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        model.addColumn("Status");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Akses");
        try{
            String sql = "SELECT * FROM tb_kasir WHERE id_kasir LIKE '%"+tcari.getText()+"%' OR nama LIKE '%"+tcari.getText()+"%' OR alamat LIKE '%"+tcari.getText()+"%' OR telepon LIKE '%"+tcari.getText()+"%' OR status LIKE '%"+tcari.getText()+"%' OR username LIKE '%"+tcari.getText()+"%' OR password LIKE '%"+tcari.getText()+"%' OR akses LIKE '%"+tcari.getText()+"%'";
            st = conn.createStatement();
            rs = st.executeQuery(sql);

            int no = 0;
            while(rs.next()){
                no++;
                model.addRow(new Object[]{
                    no, rs.getString("id_kasir"), rs.getString("nama"), rs.getString("alamat"), rs.getString("telepon"), rs.getString("status"), rs.getString("username"), rs.getString("password"), rs.getString("akses")
                });
            }
            tdata.setModel(model);
        } catch ( Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tcariKeyPressed

