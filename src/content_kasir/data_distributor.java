    private void tampil(){
        DefaultTableModel model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        model.addColumn("NO");;
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        try{
            String sql = "SELECT * FROM tb_distributor";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            
            int no = 0;
            while(rs.next()){
                no++;
                model.addRow(new Object[]{
                    no, rs.getString("id_distributor"), rs.getString("nama_distributor"), rs.getString("alamat"), rs.getString("telepon")
                });
            }
            tdata.setModel(model);
        } catch ( Exception e) {    
            System.out.println(e.getMessage());
        }
    }
    
    public void kode(){
        try {
            String sql = "SELECT * FROM tb_distributor ORDER BY id_distributor DESC";
            pr = conn.prepareStatement(sql);
            rs = pr.executeQuery();
            if (rs.next()) {
                String kode_otomatis = rs.getString("id_distributor").substring(1);
                String an = ""+(Integer.parseInt(kode_otomatis)+1);
                String nol = "";
                if (an.length() == 1) {
                    nol = "0000";
                }else if(an.length() == 2){
                    nol = "000";
                }else if(an.length() == 3){
                    nol = "00";
                }else if(an.length() == 4){
                    nol = "0";
                }else if(an.length() == 5){
                    nol = "";
                }
                tid.setText("D"+nol+an);
            }else{
                tid.setText("D00001");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void kosong(){
        tnama.setText("");
        talamat.setText("");
        thp.setText("");
        tcari.setText("");
        kode();
    }

